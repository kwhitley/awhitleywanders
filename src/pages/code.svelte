<p>
  So, what do I mean by "elegant code"?
</p>

<p>
  I believe code should be self-documenting and human-readable.  Types and enforcement/rules are great and all, but the bottom line is,
  if you can glance quickly at a block of code and understand it, you're going to:
</p>

<ol>
  <li>Waste less time comprehending it.</li>
  <li>Waste less time comprehending it. (not a typo)</li>
  <li>Waste less time comprehending it. (still not a typo)</li>
</ol>

<p>
  This means your future self (or any others having to read through your code) will be able to make your changes without staring blankly
  at the screen or wasting time looking up the answer in extensive documentation.  As a lazy developer myself, I'd rather write code with
  readable names, obvious flow, not-overly-clever bits (I love code-golfing, but let's be real - it makes for an unreadable mess), and a
  minimal bit of commenting here and there.
</p>

<p>
  Much like a school paper or company memo, less is typically more.  No one really wants to read 5 lines, when one will suffice.
</p>

<p>
  As an open-source developer, I write libs to make downstream code lightweight, readable, and <em>obvious</em>.  As a mentor and technical
  advisor, I guide along this path as well!
</p>

<blockquote>
  The faster we [are able to] move, the faster we can fail - and the faster we can win.
</blockquote>
